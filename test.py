from tinyriscv import RISCV

riscv = RISCV()
riscv.addi(1, 2, 10)
riscv.addi(1, 2, 10000000)
riscv.addi(2, 3, 2)
riscv.sll(3, 1, 2)
riscv.srl(4, 3, 2)
riscv.slli(5, 4, 1)
riscv.srai(6, 5, 1)
riscv.xor(2, 3, 2)
print("===============hex===============")
riscv.dump(view=True)
print("===============bin===============")
riscv.dump(format="bin", view=True)
print("===============oct===============")
riscv.dump(format="oct", view=True)
print(riscv.regmap())
